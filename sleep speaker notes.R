# Speaker Notes: Sleep Across the Lifespan

## Slide 1: Sleep Across the Lifespan

Opening question:
  
  "How many hours did everyone sleep last night?"

Ask for a show of hands:
  
  * Less than 5
* 5-6
* 7-8
* More than 8

Point out that almost everyone believes sleep is important, yet many people intentionally sacrifice it.

Introduce the central question:
  
  Why would evolution allow organisms to spend roughly one-third of their lives unconscious and vulnerable?
  
  Today we will look at:
  
  * How scientists study sleep
* What happens during sleep
* Why we think sleep exists
* How sleep changes across development
* What happens when sleep systems malfunction

---
  
  ## Slide 2: Why Study Sleep?
  
  Students often think of sleep as simply rest.

Modern research suggests sleep affects:
  
  * Learning
* Memory
* Emotional regulation
* Physical health
* Cognitive aging

Ask:
  
  "If I offered you an extra two hours to study for an exam, but you had to lose two hours of sleep, would that help or hurt your grade?"

Most students initially think more study time is always beneficial.

This creates a natural transition into memory consolidation research.

---
  
  ## Slide 3: Why Do We Sleep?
  
  One of the most important ideas:
  
  Scientists do not agree on a single reason for sleep.

Instead, sleep probably serves several functions simultaneously.

Think of sleep like eating.

Eating serves many purposes:
  
  * Energy
* Growth
* Repair
* Immune function

Sleep likely does the same.

---
  
  ## Slide 4: Measuring Sleep
  
  Introduce polysomnography.

Students often imagine scientists simply watch sleeping people.

Instead, sleep research relies heavily on physiological measures.

Briefly explain:
  
  EEG = brain activity

EOG = eye movements

EMG = muscle activity

Together these measures allow researchers to identify sleep stages.

---
  
  ## Slide 5: EEG Basics
  
  Emphasize an important scientific point.

EEG does not measure thoughts.

EEG measures electrical activity produced by large populations of neurons.

This distinction helps reinforce ideas about operational definitions.

Scientists cannot directly observe thoughts.

They infer mental processes from measurable variables.

---
  
  ## Slide 6: Brain Waves
  
  Do not spend excessive time on frequencies.

Focus on the pattern.

As sleep deepens:
  
  * Brain activity becomes slower
* Brain activity becomes more synchronized

Delta waves are especially important because they dominate deep sleep.

We will revisit delta activity later when discussing aging and glymphatic function.

---
  
  ## Slide 7: Sleep Stages
  
  Sleep is not one uniform state.

Instead, we cycle through multiple states repeatedly throughout the night.

Think of sleep as a sequence rather than a single condition.

---
  
  ## Slide 8: NREM Stage 1
  
  Ask:
  
  "Have you ever felt like you were falling just as you were drifting off to sleep?"

Many students have experienced hypnic jerks.

This provides an example of a common phenomenon occurring during Stage 1 sleep.

---
  
  ## Slide 9: NREM Stage 2
  
  Stage 2 occupies most of adult sleep.

Introduce sleep spindles.

Researchers believe sleep spindles may be involved in learning and memory consolidation.

Students do not need detailed neurophysiology.

The important idea is that sleep involves active brain processes.

---
  
  ## Slide 10: NREM Stage 3
  
  This is deep sleep.

Key ideas:
  
  * Difficult to awaken
* Growth hormone release
* Physical restoration
* Immune support

Many restorative theories of sleep focus heavily on this stage.

---
  
  ## Slide 11: REM Sleep
  
  Introduce paradoxical sleep.

The brain appears highly active.

The body is largely immobilized.

Ask students:
  
  "What problems might occur if REM paralysis failed?"

This creates a natural transition to REM Sleep Behavior Disorder later in the lecture.

---
  
  ## Slide 12: Sleep Cycles
  
  Draw attention to the fact that sleep architecture changes across the night.

Early night:
  
  * More deep sleep

Late night:
  
  * More REM sleep

Ask:
  
  "What happens if someone regularly sleeps only four hours?"

Students often realize they may be losing disproportionately large amounts of REM sleep.

---
  
  ## Slides 13–16: Major Theories of Sleep
  
  Avoid presenting theories as competitors.

Present them as potentially complementary explanations.

Use the analogy:
  
  "Why do we eat?"

There is not one answer.

Similarly, there may not be one reason for sleep.

Possible functions include:
  
  * Restoration
* Energy conservation
* Learning
* Neural maintenance

---
  
  ## Slide 17: Glymphatic System
  
  This is one of the most exciting recent developments.

The brain lacks a traditional lymphatic system.

Researchers discovered fluid movement pathways that appear to help remove metabolic waste.

Popular media often describes this as the brain's cleaning crew.

That metaphor is useful but somewhat simplified.

---

## Slide 18: Brain Maintenance Theory

Explain the central finding.

During sleep, especially deep sleep:

* Cerebrospinal fluid movement increases
* Waste products may be removed more efficiently

Researchers are especially interested in:

* Beta-amyloid
* Tau proteins

Both are implicated in Alzheimer's disease.

Important caveat:
  
  This does not mean sleep completely prevents Alzheimer's disease.

The evidence suggests sleep may contribute to brain maintenance.

---

## Slide 19: Circadian Rhythms

Distinguish between:

Sleep pressure

and

Circadian timing

Students often confuse these concepts.

You can be:

* Very tired but biologically not ready to sleep
* Not tired but biologically scheduled for sleep

---

## Slide 20: Sleep Across the Lifespan

Introduce the developmental perspective.

Sleep changes substantially across age.

This is why sleep belongs in a lifespan psychology course.

---

## Slide 21: Infancy and Childhood

Infants spend enormous amounts of time asleep.

Possible explanations:

* Rapid brain development
* Learning
* Neural organization

Connect to developmental plasticity.

---

## Slide 22: Adolescence

This is often students' favorite section.

Explain circadian phase delay.

Teenagers naturally become sleepy later.

This is a biological shift, not merely laziness.

Use this section to discuss school start-time debates.

---
  
  ## Slide 23: Adulthood and Aging
  
  Older adults often experience:
  
  * Less deep sleep
* More awakenings
* More fragmented sleep

Connect back to glymphatic theory.

One current question:
  
  Could age-related reductions in deep sleep contribute to cognitive decline?
  
  ---
  
  ## Slide 24: Insomnia
  
  Distinguish occasional poor sleep from chronic insomnia.

Most students have experienced sleep difficulties.

Many have not experienced a true sleep disorder.

---
  
  ## Slide 25: Sleep Apnea
  
  Discuss:
  
  * Interrupted breathing
* Reduced oxygen
* Daytime fatigue

Students are often surprised how common sleep apnea is.

---
  
  ## Slide 26: Narcolepsy
  
  Introduce orexin (hypocretin).

Narcolepsy illustrates that wakefulness itself requires active biological systems.

Remaining awake is not simply the absence of sleep.

---
  
  ## Slide 27: REM Sleep Behavior Disorder
  
  Return to earlier discussion of REM paralysis.

Normally the body is largely immobilized during REM.

In this disorder that mechanism fails.

People may physically enact dreams.

---
  
  ## Slide 28: Sleep Deprivation
  
  Ask students:
  
  "Would you drive after drinking?"

Most say no.

Then discuss evidence showing severe sleep deprivation can impair performance similarly to alcohol intoxication.

Students often underestimate this risk.

---
  
  ## Slide 29: Discussion
  
  Possible discussion questions:
  
  Why do people voluntarily sacrifice sleep?
  
  Should high schools start later?
  
  Would society benefit from treating sleep as a public-health issue?
  
  How much evidence would we need before concluding sleep protects the aging brain?
  
  ---
  
  ## Slide 30: Closing
  
  Three major takeaways:
  
  1. Sleep is an active biological process.

2. Sleep likely serves multiple functions.

3. Sleep changes throughout the lifespan but remains essential at every age.

Final question:
  
  If sleep is so important, why is it often the first thing people give up?
  